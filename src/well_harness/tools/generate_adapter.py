def _to_pythonIdentifier(s: str) -> str:
    """Convert any string to a valid Python identifier."""
    import re
    s = re.sub(r"[^a-zA-Z0-9_]", "_", s)
    s = re.sub(r"^[^a-zA-Z]+", "", s)
    return s or "_"


def _to_pythonFilename(s: str) -> str:
    """Convert system_id to a safe filename."""
    return _to_pythonIdentifier(s).lower()


