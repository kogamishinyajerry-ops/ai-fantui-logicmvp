def spec_to_adapter_source(spec: dict[str, Any], *, source_path: str = "") -> str:
    """
    Convert a parsed control_system_spec_v1 dict into the source text of
    a complete Python adapter file.
    """
    system_id: str = spec["system_id"]
    title: str = spec.get("title", system_id)
    adapter_id: str = _to_pythonIdentifier(system_id) + "-controller-adapter"
    class_name: str = _to_pythonIdentifier(system_id) + "_controller_adapter"
    ts = datetime.datetime.now().isoformat()
    sp = source_path or "(in-memory)"
    spec_filename = _to_pythonFilename(system_id)
    pkg = __name__.rsplit(".", 1)[0]

    # Build logic evaluation source lines and track comparison ops used
    eval_lines, comparisons_used = _generate_evaluate_snapshot(spec, system_id)

    # Condition helper methods (for special comparisons like between_exclusive)
    helper_lines = _generate_condition_helpers(comparisons_used)

    # ── Assemble the complete file line-by-line ───────────────────────────
    out: list[str] = []

    def L(line: str = "") -> None:
        out.append(line)

    L(f'''"""
Auto-generated Python adapter — AI FANTUI LogicMVP P17-NLC-A
==============================================================
  system_id : {system_id}
  title     : {title}
  source    : {sp}
  generated : {ts}

DO NOT EDIT THIS FILE DIRECTLY.
It was generated by: well_harness.tools.generate_adapter
Re-generate after editing the source spec JSON.
"""''')
    L()
    L("from __future__ import annotations")
    L()
    L("from dataclasses import dataclass")
    L("from typing import Any, Mapping")
    L()
    L("from well_harness.controller_adapter import (")
    L("    ControllerTruthMetadata,")
    L("    GenericTruthEvaluation,")
    L(")")
    L()
    L()
    L("@dataclass(frozen=True)")
    L("class ControllerTruthMetadata:")
    L("    adapter_id: str")
    L("    system_id: str")
    L("    truth_kind: str")
    L("    source_of_truth: str")
    L("    description: str")
    L()
    L()
    L("@dataclass(frozen=True, eq=False)")
    L("class GenericTruthEvaluation:")
    L("    system_id: str")
    L("    active_logic_node_ids: tuple[str, ...]")
    L("    asserted_component_values: dict[str, Any]")
    L("    completion_reached: bool")
    L("    blocked_reasons: tuple[str, ...]")
    L("    summary: str")
    L()
    L()
    L(f"CONTROLLER_METADATA = ControllerTruthMetadata(")
    L(f'    adapter_id="{adapter_id}",')
    L(f'    system_id="{system_id}",')
    L('    truth_kind="spec-derived-generic-truth",')
    L(f'    source_of_truth="{sp}",')
    L(f'    description="Auto-generated GenericControllerTruthAdapter for {title}",')
    L(")")
    L()
    L()
    L(f"class {class_name}:")
    L(f'    """')
    L(f'    Spec-derived GenericControllerTruthAdapter.')
    L(f'    ')
    L(f'    Implements GenericControllerTruthAdapter by evaluating the logic_nodes')
    L(f'    defined in the embedded spec dict directly against the incoming snapshot.')
    L(f'    ')
    L(f'    AUTO-GENERATED by tools/generate_adapter.py — do not edit by hand.')
    L(f'    """')
    L()
    L("    def __init__(self, spec: dict[str, Any] | None = None) -> None:")
    L("        self.spec = spec or self._load_spec_dict()")
    L("        self._logic_rules = self._build_logic_rules(self.spec)")
    L("        self._refresh_rules()")
    L()
    L("    def _refresh_rules(self) -> None:")
    L("        self._logic_rules = {")
    L("            ln['id']: {")
    L("                'conditions': ln.get('conditions', []),")
    L("                'downstream_component_ids': tuple(ln.get('downstream_component_ids', [])),")
    L("            }")
    L("            for ln in self.spec.get('logic_nodes', [])")
    L("        }")
    L()
    L("    @property")
    L("    def metadata(self) -> ControllerTruthMetadata:")
    L("        return CONTROLLER_METADATA")
    L()
    L("    def load_spec(self) -> dict[str, Any]:")
    L("        return dict(self.spec)")
    L()
    L("    def evaluate_snapshot(self, snapshot: Mapping[str, Any]) -> GenericTruthEvaluation:")
    L("        snap = dict(snapshot)")
    L()
    for line in eval_lines:
        L(line)  # eval_lines already carry correct method-body indentation
    L()
    L("    @staticmethod")
    L("    def _resolve_param(value: Any, snap: dict) -> Any:")
    L("        if isinstance(value, str) and value in snap:")
    L("            return snap[value]")
    L("        return value")
    L()
    if helper_lines and helper_lines != ["    pass  # no special comparison operators used"]:
        for line in helper_lines:
            L(line)
    L()
    L("    @staticmethod")
    L("    def _build_logic_rules(spec: dict) -> dict:")
    L("        return {")
    L("            ln['id']: {")
    L("                'conditions': ln.get('conditions', []),")
    L("                'downstream_component_ids': tuple(ln.get('downstream_component_ids', [])),")
    L("            }")
    L("            for ln in spec.get('logic_nodes', [])")
    L("        }")
    L()
    L("    @staticmethod")
    L("    def _load_spec_dict() -> dict:")
    L("        import json, pathlib")
    L(f"        _spec_path = pathlib.Path(__file__).parent / 'specs' / '{spec_filename}.spec.json'")
    L("        return json.loads(_spec_path.read_text())")
    L()
    L("    def __repr__(self) -> str:")
    L('        return f"{self.__class__.__name__}(system_id={self.metadata.system_id!r})"')
    L()

    return "\n".join(out)


def write_adapter_from_spec(
    spec_path: str | Path,
    output_path: str | Path,
    *,
    source_path: str | None = None,
) -> None:
    """
    Read spec_path, write adapter source to output_path.
    Raises SystemExit on error.
    """
    spec_path = Path(spec_path)
    output_path = Path(output_path)

    if not spec_path.exists():
        print(f"ERROR: spec not found: {spec_path}", file=sys.stderr)
        sys.exit(1)

    try:
        spec = json.loads(spec_path.read_text())
    except json.JSONDecodeError as exc:
        print(f"ERROR: invalid JSON in {spec_path}: {exc}", file=sys.stderr)
        sys.exit(1)

    src = spec_to_adapter_source(spec, source_path=str(spec_path))
    try:
        output_path.write_text(src)
    except OSError as exc:
        print(f"ERROR: failed to write {output_path}: {exc}", file=sys.stderr)
        sys.exit(2)

    print(f"Wrote: {output_path}")


