def _main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate a Python adapter from a control_system_spec_v1 JSON file.",
    )
    parser.add_argument(
        "spec",
        type=Path,
        help="Path to the control_system_spec_v1 JSON file",
    )
    parser.add_argument(
        "-o", "--output",
        type=Path,
        default=None,
        help="Output path (default: <system_id>_adapter.py next to spec)",
    )
    args = parser.parse_args()

    output = args.output
    if output is None:
        stem = args.spec.stem
        output = args.spec.parent / f"{stem}_adapter.py"

    write_adapter_from_spec(args.spec, output)


if __name__ == "__main__":
    _main()
